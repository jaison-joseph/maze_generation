def traceBack(lk, fitness):
    size_ = 30
    nextSteps = []
    stacks = [[[0, 0]]]
    print("initial stacks: ", stacks)
    newStacks = []
    # print("[0]: ", stacks[0])

    # stacks.forEach(s => {console.log("stack: ", s)})

    while(len(stacks[0]) != fitness) :
        newStacks = []
        for stack in stacks:
            # print("stack: ", stack)
            pt = stack[-1]
            dist = lk[pt[0]][pt[1]]
            dist += 1
            nextSteps = []
    
            foo = [pt[0]+1, pt[1]]
            if (foo[0] < size_ and lk[foo[0]][foo[1]] == dist):
                nextSteps.append(foo)
        
            foo = [pt[0], pt[1]+1]
            if (foo[1] < size_ and lk[foo[0]][foo[1]] == dist):
                nextSteps.append(foo)
        
            foo = [pt[0]-1, pt[1]]
            if (foo[0] >= 0 and lk[foo[0]][foo[1]] == dist):
                nextSteps.append(foo)
        
            foo = [pt[0], pt[1]-1]
            if (foo[1] >= 0 and lk[foo[0]][foo[1]] == dist):
                nextSteps.append(foo)
        
            for s in nextSteps:
                stack.append(s)
                newStacks.append(stack.copy())
                stack.pop()
    
        # print("newStacks: ", newStacks)
        stacks = newStacks
        print("new length: ", len(stacks[0]))

    print("stacks: ", len(stacks))
    # list(map(print, stacks))
    return next(filter(lambda x: x[-1] == [29,29], stacks))

